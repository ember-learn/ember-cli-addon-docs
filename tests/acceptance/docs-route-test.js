import { module, test } from 'qunit';
import { setupApplicationTest } from 'ember-qunit';
import setupMirage from 'ember-cli-mirage/test-support/setup-mirage';
import { visit, click, currentURL } from '@ember/test-helpers';

module('Acceptance | Docs route test', function (hooks) {
  setupApplicationTest(hooks);
  setupMirage(hooks);

  test('the docs route renders', async function (assert) {
    await visit('/docs');

    assert.strictEqual(currentURL(), '/docs');
  });

  test('I can visit an autogenerated docs route', async function (assert) {
    await visit('/docs');
    await click('[data-test-label="<DocsHero/\u200B>"]');

    assert.dom('h1').hasText('DocsHero');
  });

  test('I can visit a nested child route within the docs pages and still have the correct links', async function (assert) {
    await visit('/sandbox/docs/one/child');
    assert.dom('[data-test-next-link] > a').hasText('Two');
  });
});
